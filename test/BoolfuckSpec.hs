module BoolfuckSpec where
  
import Boolfuck 
import Test.Hspec
import Data.Char (ord)

spec :: Spec
spec = do
  describe "interpret Boolfuck" $ do
    it "Empty tests" $ do
      boolfuck "" "" `shouldBe` ""

    it "Single command tests" $ do
      boolfuck "<" "" `shouldBe` ""
      boolfuck ">" "" `shouldBe` ""
      boolfuck "+" "" `shouldBe` ""
      boolfuck "." "" `shouldBe` ""
      boolfuck ";" "" `shouldBe` "\x0000"

